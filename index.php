<script>
   $(document).ready(function(){
      var timeout;
      $('#search').keyup(function(){
         var query = $(this).val();
         if (query.length > 2) {
               $.ajax({
                  url: 'search-game.php',
                  method: 'POST',
                  data: {query: query},
                  success: function(data){
                     $('#result-games').html(data);
                     if ($('#result-games:contains("Nenhum game foi encontrado")').length) {
                           clearTimeout(timeout);
                           timeout = setTimeout(function(){
                              $('#result-games').empty();
                              $('#search').val('');
                           }, 10000);
                     }
                  }
               });
         } else {
               $('#result-games').empty();
         }
      });
   });
</script>
